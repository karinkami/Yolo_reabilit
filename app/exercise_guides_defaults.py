    if not g:
        return {
            "title": key.replace("_", " ").title(),
            "summary": "Следуйте рекомендациям врача и подсказкам во время подхода.",
            "what_counts": "",
            "how_to": [
                "Встаньте так, чтобы рабочая часть тела была в кадре.",
                "Двигайтесь плавно; при боли остановитесь.",
            ],
            "mistakes": [],
        }
    return {
        "title": g["title"],
        "summary": g["summary"],
        "what_counts": g.get("what_counts") or "",
        "how_to": list(g["how_to"]),
        "mistakes": list(g.get("mistakes") or []),
    }
